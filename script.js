function add(firstDigit, secondDigit) {
    return firstDigit+secondDigit
}

function substract(firstDigit, secondDigit) {
    return firstDigit-secondDigit
}

function multiply(firstDigit, secondDigit) {
    return firstDigit*secondDigit
}

function divide(firstDigit, secondDigit) {
    return firstDigit/secondDigit
}